import os
from telegram.ext import Updater, CommandHandler, MessageHandler, Filters
from openai import OpenAI

BOT_TOKEN = os.getenv("BOT_TOKEN")
OPENAI_API_KEY = os.getenv("OPENAI_API_KEY")

client = OpenAI(api_key=OPENAI_API_KEY)

# –ü—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏–µ
WELCOME = (
    "–ê—Å—Å–∞–ª—è–º—É –ê–ª–µ–π–∫—É–º —É–∞ –†–∞—Ö–º–∞—Ç—É–õ–ª–∞—Ö–∏ —É–∞ –ë–∞—Ä–∞–∫—è—Ç—É—Ö! üëãüèª\n\n"
    "–î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å –≤ –ø—Ä–æ—Å—Ç—Ä–∞–Ω—Å—Ç–≤–æ, –≥–¥–µ –°–µ—Ä–¥—Ü–µ —É–∑–Ω–∞—ë—Ç —Å–µ–±—è –∑–∞–Ω–æ–≤–æ.\n\n"
    "–ß—Ç–æ–±—ã –Ω–∞—á–∞—Ç—å, –ø—Ä–æ—Å—Ç–æ –Ω–∞–ø–∏—à–∏ –º–Ω–µ –ª—é–±–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ. üåø"
)

def start(update, context):
    update.message.reply_text(WELCOME)

def reply(update, context):
    user_text = update.message.text

    completion = client.chat.completions.create(
        model="gpt-5-mini",
        messages=[
            {"role": "system", "content": "You are a helpful assistant."},
            {"role": "user", "content": user_text}
        ]
    )

    answer = completion.choices[0].message["content"]
    update.message.reply_text(answer)

def main():
    updater = Updater(BOT_TOKEN, use_context=True)
    dp = updater.dispatcher

    dp.add_handler(CommandHandler("start", start))
    dp.add_handler(MessageHandler(Filters.text & ~Filters.command, reply))

    updater.start_polling()
    updater.idle()

if __name__ == "__main__":
    main()
